/**
 * Клинические пресеты раны.
 *
 * Каждый пресет — это «начальная точка» для формы параметров: клик подменяет
 * соответствующие поля в `useSimulationStore.params`. Пользователь может
 * корректировать любое поле вручную после применения.
 *
 * Биологическое обоснование выбора значений (без научных ссылок в UI —
 * только в коде, для разработчиков):
 *
 * - Healthy control — нормальная острая рана, физиологический баланс M1/M2,
 *   нормоксия, заживление за 3-4 недели без вмешательства.
 * - Diabetic — застрявшая в воспалительной фазе диабетическая язва (DFU):
 *   гиперактивация M1, нейтропения, гипоксия, дефицит IL-10, повышенный TNF.
 * - Chronic wound — общий хронический сценарий с включёнными PRP+PEMF
 *   (используется как демонстрационный по умолчанию).
 *
 * Структура: каждый пресет задаёт группу полей, которая интересна для данного
 * сценария. Базовые ECM/cytokine значения задаются явно, чтобы не «утекали»
 * остатки от предыдущего пресета (Dashboard вызывает resetParams() перед
 * применением).
 */

export type PresetName = 'Chronic wound' | 'Healthy control' | 'Diabetic';

export interface PresetMeta {
  name: PresetName;
  /** Краткое описание для tooltip / accessible label (не публикация). */
  description: string;
}

